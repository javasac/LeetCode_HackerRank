package com.dsa.LeetCode.Rectangle;

public class Origin
{
    private int X;
    private int Y;

    public Origin(int x, int y) {
        X = x;
        Y = y;
    }

    public int getX() {
        return X;
    }

    public void setX(int x) {
        X = x;
    }

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }
}
